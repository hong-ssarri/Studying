package day06;

public class Three_operator {
	public static void main(String[] args) {
		int a = 17; 
		int b = 5;
		
		b=a; //b=3;
		
		System.out.println(b);
		
		
		a+=1;//a=a+1;
		System.out.println(a);//18
		
		a/=2;
		System.out.println(a);//9
		
		a*=a; //a=a*9
		System.out.println(a); //81
	}

}
